
// loop นี้จะทำทั้งหมด 50 รอบ โดยจะเริ่มที่ 1 เเละจบที่ 50
// loop นี้สำหรับ วนเเม่สูตรคูณ
for(let i = 1; i <= 50; i++ ){
    console.log(`===================== เเม่ ${i} ======================`);

    // เมื่อ Loop เเรกทำงานจะลงมาทำ Loop ด้านล้างต่อ
    // Loop นี้สำหรับวนภายในเเม่น้ันๆ โดยจะเริ่มที่ 1 เเละจบที่ 12
    for(let ii = 1; ii <= 12; ii++){
        
        //จากนั้นให้เเสดง ออกมา  
        console.log(`${i} X ${ii} = ${i * ii}`);
    }
}